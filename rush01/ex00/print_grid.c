#include <unistd.h>

void	print_grid(int **grid)
{
	int	i;
	int	j;
	char	k;

	i = 0;
	k = '0';
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			k = '0' + grid[i][j];
			write(1, &k, 1);
			write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
