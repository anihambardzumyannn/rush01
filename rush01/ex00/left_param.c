#include <stdlib.h>

int	*left_param(int **paras)
{
	int	*row_left;
	int	i;

	row_left = (int *)malloc(4 * 8);
	i = 0;
	while (i < 4)
	{
		row_left[i] = paras[2][i];
		i++;
	}
	return (row_left);
}
